public class Caracteristicas {

    private String fabricante;
    private String do_region;
    private String coupage;
    private String alcohol;
    private String capacidad;

    public String getFabricante() {
        return fabricante;
    }

    public void setFabricante(String fabricante) {
        this.fabricante = fabricante;
    }

    public String getDo_region() {
        return do_region;
    }

    public void setDo_region(String do_region) {
        this.do_region = do_region;
    }

    public String getCoupage() {
        return coupage;
    }

    public void setCoupage(String coupage) {
        this.coupage = coupage;
    }

    public String getAlcohol() {
        return alcohol;
    }

    public void setAlcohol(String alcohol) {
        this.alcohol = alcohol;
    }

    public String getCapacidad() {
        return capacidad;
    }

    public void setCapacidad(String capacidad) {
        this.capacidad = capacidad;
    }
}
